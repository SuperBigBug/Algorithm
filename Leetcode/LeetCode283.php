class LeetCode283 {

    /**
     * @param Integer[] $nums
     * @return NULL
     */
    function moveZeroes(&$nums) {
        // åŽŸå§‹é•¿åº¦
        $len = count($nums);
        // æ“ä½œåŽé•¿åº¦
        $newLen = $this->removeDuplicates($nums, 0);
        for (; $newLen < $len; $newLen++) {
            $nums[$newLen] = 0;
        }
        return null;
    }

    function removeDuplicates(&$nums, $val) {
        $len = count($nums);
        if ($len < 1) {
            return false;
        }
        $fast = $slow = 0;
        while($fast < $len) {
            if ($nums[$fast] != $val) {
                $nums[$slow] = $nums[$fast];
                $slow++;
            }
            $fast++;
        }
        return $slow;
    }
}
